import { routerRedux } from 'dva/router';
import { message } from 'antd';
import { queryGroupons } from '@/services/api';


export default {
  namespace: 'groupon',

  state: {
    data: {
      results: [],
      count: undefined,
    },
  },

  effects: {
    *fetch({ payload }, { call, put }) {
      const response = yield call(queryGroupons, payload);
      yield put({
        type: 'save',
        payload: response,
      });
    },
  },

  reducers: {
    save(state, action) {
      return {
        ...state,
        data: action.payload,
      };
    },
  },
};
